//  ASCII art sprites for all 18 pet species, ported from buddy sprites.ts
//  Each species has 3 frames, 5 lines tall, 12 chars wide.
//  {E} is replaced with the actual eye character.
//

import Foundation

enum PetSprites {

    /// Returns 3 rendered frames for the given species/eye/hat combo.
    static func frames(for species: PetSpecies, eye: PetEye, hat: PetHat) -> [[String]] {
        let raw = rawFrames[species]!
        return raw.map { frame in
            var lines = frame.map { $0.replacingOccurrences(of: "{E}", with: eye.rawValue) }
            // Apply hat to line 0 if the slot is blank
            if hat != .none, lines[0].trimmingCharacters(in: .whitespaces).isEmpty {
                lines[0] = hatLines[hat]!
            }
            return lines
        }
    }

    /// Compact face string for inline display.
    static func face(for species: PetSpecies, eye: PetEye) -> String {
        let e = eye.rawValue
        switch species {
        case .duck, .goose:  return "(\(e)>"
        case .blob:          return "(\(e)\(e))"
        case .cat:           return "=\(e)\u{03C9}\(e)="
        case .dragon:        return "<\(e)~\(e)>"
        case .octopus:       return "~(\(e)\(e))~"
        case .owl:           return "(\(e))(\(e))"
        case .penguin:       return "(\(e)>)"
        case .turtle:        return "[\(e)_\(e)]"
        case .snail:         return "\(e)(@)"
        case .ghost:         return "/\(e)\(e)\\"
        case .axolotl:       return "}\(e).\(e){"
        case .capybara:      return "(\(e)oo\(e))"
        case .cactus:        return "|\(e)  \(e)|"
        case .robot:         return "[\(e)\(e)]"
        case .rabbit:        return "(\(e)..\(e))"
        case .mushroom:      return "|\(e)  \(e)|"
        case .chonk:         return "(\(e).\(e))"
        case .fox:           return "/\(e) \(e)\\"
        case .bunny2:        return ".(\(e) \(e))."
        case .owl2:          return "(\(e) \(e))"
        case .koala:         return "{\(e) \(e)}"
        case .tuxPenguin:    return "|\(e)_\(e)|"
        case .elephant:      return "((\(e) \(e)))"
        case .sheep:         return "U\(e)\(e)U"
        case .squirrel:      return "/\(e) \(e)\\"
        case .bear:          return "(\(e)  \(e))"
        case .dragon2:       return "/\(e) \(e)\\"
        case .kitty:         return "(\(e) \(e))"
        case .cowsayCat:     return "o\(e)\(e)"
        case .crab:          return "/\(e)_\(e)\\"
        }
    }

